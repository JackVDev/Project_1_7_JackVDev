def type_calc(type_atk=18, type_def=18):
    """A simple calculator for pokemon type effectiveness.

    Args:
        type_atk (int, optional): The index of the attacking type. Defaults to 18 (Typeless).
        type_def (int, optional): The index of the defending type. Defaults to 18 (Typeless).

    Returns:
        int: The type effectiveness multiplier for the specified matchup. Returns 1 if either input is 18 (Typeless).
    """
    if type_atk == 18 or type_def == 18:
        return 1
    else:
        return type_chart[type_atk][type_def]